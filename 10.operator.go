package main

import "fmt"

func main() {

	// Operator Aritmatika
	var result = 20 + 30

	fmt.Println(result)

	var a = 20
	var b = 30
	var c = a * b
	fmt.Println(c)

	// Augmented Assignments
	var i = 20
	i += 40 // i = i + 20

	fmt.Println(i)

	// Unary Operator

	i++ // i = i + 1
	fmt.Println(i)
}
